    public void getEstadisticas(){
        actual = jScrollPane1.getViewport().getViewPosition();
        actualD = jScrollPane1.getViewport().getExtentSize();
        EstadisticasImagen ei = new EstadisticasImagen();
        ei.calculaEstadisticas(bufferedImageToMat(actualIm), actual, actualD);
        maxR.setText(String.valueOf(ei.getMaximo()[0]));
        minR.setText(String.valueOf(ei.getMinimo()[0]));
        promR.setText(String.valueOf(ei.getPromedio()[0])); 
        maxG.setText(String.valueOf(ei.getMaximo()[1]));
        minG.setText(String.valueOf(ei.getMinimo()[1]));
        promG.setText(String.valueOf(ei.getPromedio()[1])); 
        maxB.setText(String.valueOf(ei.getMaximo()[2]));
        minB.setText(String.valueOf(ei.getMinimo()[2]));
        promB.setText(String.valueOf(ei.getPromedio()[2]));       
    }
    private Mat bufferedImageToMat(BufferedImage bi) {
            Mat mat = new Mat(bi.getHeight(), bi.getWidth(), CvType.CV_8UC3);
            byte[] data = ((DataBufferByte) bi.getRaster().getDataBuffer()).getData();
            mat.put(0, 0, data);
            return mat;
        }

